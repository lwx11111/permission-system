@Slf4j
public class NetworkUtils {
    public static String getLocalIp() {
        try {
            InetAddress addr = InetAddress.getLocalHost();
            byte[] ipAddr = addr.getAddress();
            String ipAddrStr = "";
            for (int i = 0; i < ipAddr.length; i++) {
                if (i > 0) {
                    ipAddrStr += ".";
                }
                ipAddrStr += ipAddr[i] & 0xFF;
            }
            return ipAddrStr;
        } catch (Exception e) {
            log.error("获取本地ip地址异常", e);
            return "";
        }
    }
}
